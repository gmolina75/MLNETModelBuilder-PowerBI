# Archivos binarios
*.docx binary
*.xlsx binary
*.pbix binary
*.mlnet binary
*.zip binary
*.png binary
*.jpg binary
*.jpeg binary
*.gif binary
*.ico binary
EOF
    print_success "Archivo .gitattributes creado."
else
    print_warning "Ya existe un archivo .gitattributes. No se sobrescribió."
fi

if [[ ! -f ".gitignore" ]]; then
    cat > .gitignore <<'EOF'
