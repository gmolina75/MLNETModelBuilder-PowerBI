#!/usr/bin/env bash
#
# Crea un repositorio en GitHub y sube este proyecto.
#
# Uso:
#   ./push-to-github.sh
#   ./push-to-github.sh --repo-name MiProyectoML --visibility private
#

set -euo pipefail

# -----------------------------------------------------------------------------
# Configuración por defecto
# -----------------------------------------------------------------------------
REPO_NAME="$(basename "$(pwd)")"
VISIBILITY="public"
CLEAN_TEMP_FILES=1

# -----------------------------------------------------------------------------
# Funciones auxiliares
# -----------------------------------------------------------------------------
print_step() {
    echo -e "\n\033[1;36m==> $1\033[0m"
}

print_success() {
    echo -e "\033[1;32m    ✔ $1\033[0m"
}

print_warning() {
    echo -e "\033[1;33m    ⚠ $1\033[0m"
}

print_error() {
    echo -e "\033[1;31m    ✖ $1\033[0m" >&2
}

command_exists() {
    command -v "$1" >/dev/null 2>&1
}

show_help() {
    cat <<EOF
Uso: $0 [OPCIONES]

Opciones:
  -r, --repo-name NAME      Nombre del repositorio en GitHub (por defecto: carpeta actual)
  -v, --visibility TYPE     Visibilidad: public o private (por defecto: public)
  -c, --clean BOOL          Limpiar archivos temporales: 1 o 0 (por defecto: 1)
  -h, --help                Muestra esta ayuda

Ejemplos:
  $0
  $0 -r MiProyectoML -v private
EOF
}

# -----------------------------------------------------------------------------
# Procesar argumentos
# -----------------------------------------------------------------------------
while [[ $# -gt 0 ]]; do
    case "$1" in
        -r|--repo-name)
            REPO_NAME="$2"
            shift 2
            ;;
        -v|--visibility)
            VISIBILITY="$2"
            if [[ "$VISIBILITY" != "public" && "$VISIBILITY" != "private" ]]; then
                print_error "La visibilidad debe ser 'public' o 'private'."
                exit 1
            fi
            shift 2
            ;;
        -c|--clean)
            CLEAN_TEMP_FILES="$2"
            shift 2
            ;;
        -h|--help)
            show_help
            exit 0
            ;;
        *)
            print_error "Opción desconocida: $1"
            show_help
            exit 1
            ;;
    esac
done

# -----------------------------------------------------------------------------
# 1. Verificar prerequisitos
# -----------------------------------------------------------------------------
print_step "Verificando prerequisitos..."

if ! command_exists git; then
    print_error "Git no está instalado o no se encuentra en el PATH. Descárgalo desde https://git-scm.com/downloads"
    exit 1
fi
print_success "Git encontrado."

if ! command_exists gh; then
    print_error "La CLI de GitHub (gh) no está instalada. Descárgala desde https://cli.github.com/"
    exit 1
fi
print_success "GitHub CLI (gh) encontrada."

if ! gh auth status >/dev/null 2>&1; then
    print_error "No estás autenticado en GitHub CLI. Ejecuta: gh auth login"
    exit 1
fi
print_success "Autenticado en GitHub CLI."

if [[ ! -f "AGENTS.md" ]]; then
    print_warning "No se encontró AGENTS.md en la carpeta actual. ¿Estás seguro de que este es el directorio raíz del proyecto?"
    read -rp "¿Continuar de todos modos? (s/N): " CONTINUE
    if [[ ! "$CONTINUE" =~ ^[sSyY]$ ]]; then
        exit 0
    fi
fi

# -----------------------------------------------------------------------------
# 2. Inicializar repositorio Git
# -----------------------------------------------------------------------------
print_step "Inicializando repositorio Git local..."

if [[ -d ".git" ]]; then
    print_warning "Ya existe un repositorio Git en esta carpeta. Se continuará con el remoto actual si existe."
else
    git init
    print_success "Repositorio Git inicializado."
fi

# -----------------------------------------------------------------------------
# 3. Configurar .gitignore y .gitattributes
# -----------------------------------------------------------------------------
print_step "Configurando .gitignore y .gitattributes..."

if [[ ! -f ".gitattributes" ]]; then
    cat > .gitattributes <<'EOF'
