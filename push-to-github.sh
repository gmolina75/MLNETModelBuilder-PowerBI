*.zip
EOF
    print_success "Archivo .gitignore creado."
else
    print_warning "Ya existe un archivo .gitignore. No se sobrescribió."
fi

# -----------------------------------------------------------------------------
# 4. Limpiar archivos temporales
# -----------------------------------------------------------------------------
if [[ "$CLEAN_TEMP_FILES" == "1" ]]; then
    print_step "Limpiando archivos temporales..."
    ITEMS_TO_REMOVE=(
        "temp_docx"
        "extracted_text.txt"
        "extracted_text_utf8.txt"
        "desktop.ini"
        "Tarea_Prediccion_Ventas_MLNET_ModelBuilder.zip"
    )
    for ITEM in "${ITEMS_TO_REMOVE[@]}"; do
        if [[ -e "$ITEM" ]]; then
            rm -rf "$ITEM"
            print_success "Eliminado: $ITEM"
        fi
    done
fi

# -----------------------------------------------------------------------------
# 5. Crear repositorio en GitHub
# -----------------------------------------------------------------------------
print_step "Creando repositorio remoto en GitHub..."

if gh repo view "$REPO_NAME" >/dev/null 2>&1; then
    print_warning "El repositorio '$REPO_NAME' ya existe en GitHub."
    read -rp "¿Usar el repositorio existente? (s/N): " USE_EXISTING
    if [[ ! "$USE_EXISTING" =~ ^[sSyY]$ ]]; then
        exit 0
    fi
else
    gh repo create "$REPO_NAME" --"$VISIBILITY" --source=. --remote=origin --push
    print_success "Repositorio '$REPO_NAME' creado y configurado como remoto 'origin'."
fi

# -----------------------------------------------------------------------------
# 6. Agregar, commit y push
# -----------------------------------------------------------------------------
print_step "Preparando commit inicial..."

git add .
print_success "Archivos agregados al área de preparación."

if git diff --cached --quiet; then
    print_warning "No hay cambios para commitear."
else
    git commit -m "Initial commit" -m "Proyecto MLNETModelBuilder-PowerBI subido a GitHub."
    print_success "Commit inicial realizado."

    print_step "Subiendo cambios a GitHub..."
    BRANCH=$(git branch --show-current)
    git push -u origin "$BRANCH"
    print_success "Cambios subidos a la rama '$BRANCH'."
fi

# -----------------------------------------------------------------------------
# 7. Resumen final
# -----------------------------------------------------------------------------
echo -e "\n\033[1;32m========================================"
echo -e " ¡Listo! Repositorio subido a GitHub "
echo -e "========================================\033[0m"
REMOTE_URL=$(git remote get-url origin 2>/dev/null || true)
echo -e "URL remota: \033[1;37m$REMOTE_URL\033[0m"
echo -e "\n\033[1;36mPróximos pasos:\033[0m"
echo "  1. Revisa el repositorio en GitHub."
echo "  2. Conecta el modelo real de ML.NET en SalesPredictionApp."
echo "  3. Actualiza el README.md con la descripción del proyecto."
