#requires -Version 5.1
<#
.SYNOPSIS
    Crea un repositorio en GitHub y sube este proyecto.

.DESCRIPTION
    Este script inicializa el repositorio local, configura el .gitignore,
    limpia archivos temporales, crea el repositorio remoto en GitHub
    mediante la CLI 'gh' y realiza el push inicial.

.PARAMETER RepoName
    Nombre del repositorio en GitHub. Por defecto usa el nombre de la carpeta actual.

.PARAMETER Visibility
    Visibilidad del repositorio: 'public' o 'private'. Por defecto: public.

.PARAMETER CleanTempFiles
    Si es $true (por defecto), elimina archivos temporales antes del commit inicial.

.EXAMPLE
    .\push-to-github.ps1
    .\push-to-github.ps1 -RepoName "MiProyectoML" -Visibility private
#>
[CmdletBinding(SupportsShouldProcess = $true)]
param(
    [string]$RepoName = (Split-Path -Leaf (Get-Location)),
    [ValidateSet("public", "private")]
    [string]$Visibility = "public",
    [bool]$CleanTempFiles = $true
)

$ErrorActionPreference = "Stop"

function Test-CommandExists {
    param([string]$Command)
    $null -ne (Get-Command $Command -ErrorAction SilentlyContinue)
}

function Write-Step {
    param([string]$Message)
    Write-Host "`n==> $Message" -ForegroundColor Cyan
}

function Write-Success {
    param([string]$Message)
    Write-Host "    ✔ $Message" -ForegroundColor Green
}

function Write-WarningMsg {
    param([string]$Message)
    Write-Host "    ⚠ $Message" -ForegroundColor Yellow
}

# -----------------------------------------------------------------------------
# 1. Verificar prerequisitos
# -----------------------------------------------------------------------------
Write-Step "Verificando prerequisitos..."

if (-not (Test-CommandExists "git")) {
    Write-Error "Git no está instalado o no se encuentra en el PATH. Descárgalo desde https://git-scm.com/downloads"
}
Write-Success "Git encontrado."

if (-not (Test-CommandExists "gh")) {
    Write-Error "La CLI de GitHub (gh) no está instalada. Descárgala desde https://cli.github.com/"
}
Write-Success "GitHub CLI (gh) encontrada."

# Verificar autenticación con gh
$ghAuth = gh auth status 2>&1
if ($LASTEXITCODE -ne 0) {
    Write-Error "No estás autenticado en GitHub CLI. Ejecuta: gh auth login"
}
Write-Success "Autenticado en GitHub CLI."

# Verificar que estamos en la carpeta correcta
if (-not (Test-Path "AGENTS.md")) {
    Write-WarningMsg "No se encontró AGENTS.md en la carpeta actual. ¿Estás seguro de que este es el directorio raíz del proyecto?"
    $continue = Read-Host "¿Continuar de todos modos? (s/N)"
    if ($continue -notin @("s", "S", "si", "Si", "SI")) {
        exit 0
    }
}

# -----------------------------------------------------------------------------
# 2. Inicializar repositorio Git
# -----------------------------------------------------------------------------
Write-Step "Inicializando repositorio Git local..."

if (Test-Path ".git") {
    Write-WarningMsg "Ya existe un repositorio Git en esta carpeta. Se continuará con el remoto actual si existe."
}
else {
    if ($PSCmdlet.ShouldProcess(".", "git init")) {
        git init
        Write-Success "Repositorio Git inicializado."
    }
}

# -----------------------------------------------------------------------------
# 3. Configurar .gitignore y .gitattributes
# -----------------------------------------------------------------------------
Write-Step "Configurando .gitignore y .gitattributes..."

if (-not (Test-Path ".gitattributes")) {
    @"
