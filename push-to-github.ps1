    Write-Success "Archivo .gitignore creado."
}
else {
    Write-WarningMsg "Ya existe un archivo .gitignore. No se sobrescribió."
}

# -----------------------------------------------------------------------------
# 4. Limpiar archivos temporales
# -----------------------------------------------------------------------------
if ($CleanTempFiles) {
    Write-Step "Limpiando archivos temporales..."
    $itemsToRemove = @(
        "temp_docx",
        "extracted_text.txt",
        "extracted_text_utf8.txt",
        "desktop.ini",
        "Tarea_Prediccion_Ventas_MLNET_ModelBuilder.zip"
    )
    foreach ($item in $itemsToRemove) {
        if (Test-Path $item) {
            if ($PSCmdlet.ShouldProcess($item, "Remove-Item -Recurse -Force")) {
                Remove-Item -Path $item -Recurse -Force -ErrorAction SilentlyContinue
                Write-Success "Eliminado: $item"
            }
        }
    }
}

# -----------------------------------------------------------------------------
# 5. Crear repositorio en GitHub
# -----------------------------------------------------------------------------
Write-Step "Creando repositorio remoto en GitHub..."

$repoExists = gh repo view "$RepoName" 2>&1
if ($LASTEXITCODE -eq 0) {
    Write-WarningMsg "El repositorio '$RepoName' ya existe en GitHub."
    $useExisting = Read-Host "¿Usar el repositorio existente? (s/N)"
    if ($useExisting -notin @("s", "S", "si", "Si", "SI")) {
        exit 0
    }
}
else {
    if ($PSCmdlet.ShouldProcess($RepoName, "gh repo create --$Visibility")) {
        gh repo create "$RepoName" --$Visibility --source=. --remote=origin --push
        if ($LASTEXITCODE -eq 0) {
            Write-Success "Repositorio '$RepoName' creado y configurado como remoto 'origin'."
        }
        else {
            Write-Error "No se pudo crear el repositorio en GitHub."
        }
    }
}

# -----------------------------------------------------------------------------
# 6. Agregar, commit y push
# -----------------------------------------------------------------------------
Write-Step "Preparando commit inicial..."

if ($PSCmdlet.ShouldProcess(".", "git add .")) {
    git add .
    Write-Success "Archivos agregados al área de preparación."
}

$hasChanges = git diff --cached --quiet
if ($LASTEXITCODE -ne 0) {
    if ($PSCmdlet.ShouldProcess(".", "git commit -m 'Initial commit'")) {
        git commit -m "Initial commit" -m "Proyecto MLNETModelBuilder-PowerBI subido a GitHub."
        Write-Success "Commit inicial realizado."
    }

    Write-Step "Subiendo cambios a GitHub..."
    if ($PSCmdlet.ShouldProcess("origin main", "git push")) {
        $branch = git branch --show-current
        git push -u origin "$branch"
        Write-Success "Cambios subidos a la rama '$branch'."
    }
}
else {
    Write-WarningMsg "No hay cambios para commitear."
}

# -----------------------------------------------------------------------------
# 7. Resumen final
# -----------------------------------------------------------------------------
Write-Host "`n========================================" -ForegroundColor Green
Write-Host " ¡Listo! Repositorio subido a GitHub " -ForegroundColor Green
Write-Host "========================================" -ForegroundColor Green
$remoteUrl = git remote get-url origin 2>$null
Write-Host "URL remota: $remoteUrl" -ForegroundColor White
Write-Host "`nPróximos pasos:" -ForegroundColor Cyan
Write-Host "  1. Revisa el repositorio en GitHub."
Write-Host "  2. Conecta el modelo real de ML.NET en SalesPredictionApp."
Write-Host "  3. Actualiza el README.md con la descripción del proyecto."
