*.gif binary
*.ico binary
"@ | Out-File -Encoding utf8 ".gitattributes"
    Write-Success "Archivo .gitattributes creado."
}
else {
    Write-WarningMsg "Ya existe un archivo .gitattributes. No se sobrescribió."
}

if (-not (Test-Path ".gitignore")) {
    @"
